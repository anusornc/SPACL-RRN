#!/bin/bash
# Download real-world ontologies for benchmarking
# Based on ORE 2015 competition

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ONTOLOGY_DIR="$SCRIPT_DIR/ontologies"

echo "========================================="
echo "Downloading Real-World Ontologies"
echo "========================================="

# Create directories
mkdir -p "$ONTOLOGY_DIR"/{ore2015,bioportal,other}

# ORE 2015 Benchmark Repository
# The ORE 2015 competition used ontologies from various sources
# Main repository: https://www.w3.org/wiki/Owl/Task_Forces/Reasoner/ORE_2015

echo ""
echo "Note: Direct download links for ORE 2015 are no longer active."
echo "You need to manually obtain the following ontologies:"
echo ""

cat > "$ONTOLOGY_DIR/README.md" << 'EOF'
# Real-World Ontology Benchmarks

## Required Ontologies for SPACL Evaluation

### ORE 2015 Competition Ontologies
The ORE 2015 Reasoner Evaluation used the following ontology categories:

#### 1. Biomedical Ontologies (High Priority)
- **NCI Thesaurus** (National Cancer Institute)
  - Size: ~100,000 classes
  - Profile: OWL 2 DL
  - Source: https://ncithesaurus.nci.nih.gov/
  - File: Thesaurus.owl

- **SNOMED CT** (Systematized Nomenclature of Medicine)
  - Size: ~350,000 classes
  - Profile: OWL 2 EL (but has complex extensions)
  - Note: Requires license for full version
  - Source: https://www.snomed.org/

- **Gene Ontology (GO)**
  - Size: ~45,000 classes
  - Profile: OWL 2
  - Source: http://geneontology.org/
  - File: go.owl

#### 2. General Ontologies
- **GALEN** (Medical terminology)
  - Size: ~30,000 classes
  - Profile: OWL 2 DL
  - Source: http://www.opengalen.org/

- **LUBM** (Lehigh University Benchmark)
  - Size: 43 classes (but generates large datasets)
  - Profile: OWL 2
  - Source: Included in this repo: tests/data/univ-bench.owl

#### 3. ORE 2015 Specific
Download from: https://zenodo.org/record/2570394
(or search "ORE 2015 reasoner evaluation benchmark")

Categories:
- Classification (TBox reasoning)
- Consistency checking
- Instance checking (ABox reasoning)
- Query answering

### Alternative Sources

#### BioPortal
https://bioportal.bioontology.org/
- Download OWL files directly
- Requires API key for bulk download

#### Ontology Lookup Service (OLS)
https://www.ebi.ac.uk/ols4/
- REST API available
- Can download OWL/JSON formats

### Installation Instructions

1. Download ontologies to appropriate directories:
   ```
   ontologies/ore2015/
   ontologies/bioportal/
   ontologies/other/
   ```

2. Verify file formats (should be .owl, .rdf, or .ttl)

3. Run benchmark:
   ```
   cargo bench --bench real_world_benchmark
   ```

### File Naming Convention

Please name files descriptively:
- `nci-thesaurus.owl`
- `go-basic.owl`
- `galen-module.owl`
- etc.

### Expected Benchmark Results

Based on published ORE 2015 results:

| Ontology | Classes | Konclude (s) | Pellet (s) | HermiT (s) |
|----------|---------|--------------|------------|------------|
| NCI 1% | 2,400 | 0.8 | 12.3 | 18.5 |
| NCI 10% | 24,000 | 4.2 | 89.7 | 142.3 |
| GO | 45,000 | 2.1 | 45.2 | 67.8 |

Note: Times vary by hardware and specific classification tasks.
EOF

echo "Created README.md with instructions"
echo ""
echo "To proceed:"
echo "1. Read: $ONTOLOGY_DIR/README.md"
echo "2. Download ontologies manually (most require license/registration)"
echo "3. Place .owl files in appropriate subdirectories"
echo ""
echo "Quick start with freely available ontologies:"
echo ""

# Try to download freely available ontologies
echo "Attempting to download freely available ontologies..."

# LUBM is already in the repo
echo "✓ LUBM (univ-bench.owl) - already in tests/data/"

# Try to download GO (Gene Ontology) - freely available
echo ""
echo "Downloading Gene Ontology..."
cd "$ONTOLOGY_DIR/other"
if command -v curl &> /dev/null; then
    curl -L -o "go-basic.owl" "http://purl.obolibrary.org/obo/go/go-basic.owl" 2>/dev/null || \
        echo "  ✗ GO download failed (network issue) - download manually from http://geneontology.org/"
    if [ -f "go-basic.owl" ] && [ -s "go-basic.owl" ]; then
        echo "  ✓ GO downloaded successfully"
    else
        echo "  ✗ GO download failed"
        rm -f "go-basic.owl"
    fi
else
    echo "  curl not available - install curl to download automatically"
fi

# Create a simple test ontology if nothing else is available
echo ""
echo "Creating synthetic stress-test ontologies..."
cd "$ONTOLOGY_DIR/other"

# These are generated by the test suite, but we can create specific ones here
echo "  See tests/data/hierarchy_*.owl for generated hierarchies"

echo ""
echo "========================================="
echo "Summary"
echo "========================================="
echo "Ontology directory: $ONTOLOGY_DIR"
echo ""
echo "Next steps:"
echo "1. Review $ONTOLOGY_DIR/README.md"
echo "2. Download NCI Thesaurus, SNOMED CT (if licensed), GALEN"
echo "3. Run: cargo bench --bench real_world_benchmark"
echo ""
